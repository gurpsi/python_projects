
# Booleans are True and False

a = 3
b = 4
print(a == b)
print(a < b)
print(a != b)

# Python returns all the numbers as True except 0
print(bool(23))
print(bool(-23.9))
print(bool(23+9j))
print(bool(0)) # False

# Python return True for all the string except blank.
print(bool('Test'))
print(bool(' '))
print(bool('')) # False