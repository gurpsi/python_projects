a = 2 # Int
b = 7.0 # Float
c = 2 + 0j # Complex

# Arithmetic operators

print(a+b) # int + float, returns float

print(b-a) # float - int, returns float

print(a * 2) # int * int, returns int

print(c/b) # complex / float, returns complex

print(b//a) # for Quotient part

print(b%a) # Remainder part only